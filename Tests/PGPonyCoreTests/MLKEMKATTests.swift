    /// Byte-exact key generation: KeyGen_internal(d, z) must reproduce the
    /// ACVP-expected encapsulation key (ek) and decapsulation key (dk).
    func testMLKEM768KeyGenKAT() throws {
        var seed = Data()
        seed.append(d)          // d (32)
        seed.append(z)          // z (32)  -> seed = d‖z (64)
        XCTAssertEqual(seed.count, MLKEMService.seedBytes)

        let (pk, sk) = try MLKEMService.generateKeyPair(seed: seed)
        XCTAssertEqual(pk, ekExpected, "ML-KEM-768 ek (public key) mismatch vs ACVP")
        XCTAssertEqual(sk, dkExpected, "ML-KEM-768 dk (secret key) mismatch vs ACVP")
    }

    /// Byte-exact decapsulation: Decaps(dk, c) must reproduce the ACVP-expected
    /// shared secret K. Exercises the full decaps path including the FO
    /// re-encryption / implicit-rejection branch.
    func testMLKEM768DecapsKAT() throws {
        let ss = try MLKEMService.decapsulate(ciphertext: cDecaps, secretKey: dkDecaps)
        XCTAssertEqual(ss, kExpected, "ML-KEM-768 decapsulated shared secret mismatch vs ACVP")
    }

    /// Self-consistency: a fresh keypair round-trips (encaps then decaps agree).
    /// Covers the encapsulation path, which ACVP can't pin deterministically
    /// here because liboqs exposes no derandomized encaps.
    func testMLKEM768RoundTrip() throws {
        let (pk, sk) = try MLKEMService.generateKeyPair()
        XCTAssertEqual(pk.count, MLKEMService.publicKeyBytes)
        XCTAssertEqual(sk.count, MLKEMService.secretKeyBytes)

        let (ct, ssA) = try MLKEMService.encapsulate(publicKey: pk)
        XCTAssertEqual(ct.count, MLKEMService.ciphertextBytes)
        XCTAssertEqual(ssA.count, MLKEMService.sharedSecretBytes)

        let ssB = try MLKEMService.decapsulate(ciphertext: ct, secretKey: sk)
        XCTAssertEqual(ssA, ssB, "encaps/decaps shared secrets disagree")
    }

    /// The derandomized keygen must equal the random-path sizes and be
    /// deterministic (same seed -> same keys).
    func testMLKEM768DerandDeterministic() throws {
        var seed = Data(); seed.append(d); seed.append(z)
        let a = try MLKEMService.generateKeyPair(seed: seed)
        let b = try MLKEMService.generateKeyPair(seed: seed)
        XCTAssertEqual(a.publicKey, b.publicKey)
        XCTAssertEqual(a.secretKey, b.secretKey)
    }

    // MARK: - hex helper

    private static func hex(_ s: String) -> Data {
        var out = Data(capacity: s.count / 2)
        var idx = s.startIndex
        while idx < s.endIndex {
            let next = s.index(idx, offsetBy: 2)
            out.append(UInt8(s[idx..<next], radix: 16)!)
            idx = next
        }
        return out
    }
    private func hex(_ s: String) -> Data { Self.hex(s) }
