    func testDecryptsGnuPGLibrePGPMessage() throws {
        let key = try LibrePGPDecryptService.extractDecryptionKey(
            privateKeyData: [UInt8](secretSubkeyPacket))
        XCTAssertNotNil(key, "should extract the v5 Kyber composite subkey")

        let plaintext = try LibrePGPDecryptService.decrypt(
            messageData: [UInt8](gpgMessage),
            secretKeyData: [UInt8](secretSubkeyPacket))
        XCTAssertNotNil(plaintext.range(of: Data("hello pqc from gpg".utf8)),
                        "PGPony must decrypt GnuPG's LibrePGP PQC message")
    }

    private static func hex(_ s: String) -> Data {
        let c = s.filter { $0.isHexDigit }; var o = Data(capacity: c.count/2); var i = c.startIndex
        while i < c.endIndex { let n = c.index(i, offsetBy: 2); o.append(UInt8(c[i..<n], radix: 16)!); i = n }
        return o
    }
    private func hex(_ s: String) -> Data { Self.hex(s) }
