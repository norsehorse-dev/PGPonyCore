    func testEncryptToSqPQCKeyEmitsCiphertext() throws {
        let raw = try OpenPGPPacketParser.dearmor(sqCert)
        let packets = try OpenPGPPacketParser.parsePackets(data: Array(raw))
        var recipient: OpenPGPPacketBuilder.CompositeRecipient? = nil
        for p in packets where p.tag == 14 || p.tag == 6 {
            let b = p.body
            guard b.count >= 10 + 32 + 1184, b[0] == 6, b[5] == 35 else { continue }
            var o = 6
            let keyMatLen = (Int(b[o]) << 24) | (Int(b[o+1]) << 16) | (Int(b[o+2]) << 8) | Int(b[o+3]); o += 4
            guard keyMatLen == 32 + 1184, o + keyMatLen <= b.count else { continue }
            let x = Array(b[o..<(o+32)]); o += 32
            let ml = Array(b[o..<(o+1184)])
            var fpIn: [UInt8] = [0x9B]
            let len = UInt32(b.count)
            fpIn.append(UInt8((len >> 24) & 0xFF)); fpIn.append(UInt8((len >> 16) & 0xFF))
            fpIn.append(UInt8((len >> 8) & 0xFF)); fpIn.append(UInt8(len & 0xFF))
            fpIn.append(contentsOf: b)
            let fp = Array(SHA256.hash(data: Data(fpIn)))
            recipient = OpenPGPPacketBuilder.CompositeRecipient(subkeyFingerprint: fp, x25519Public: x, mlkemPublic: ml)
            break
        }
        let rec = try XCTUnwrap(recipient, "sq cert must contain an ML-KEM-768 + X25519 (algo 35) subkey")

        let plaintext = Data("hello pqc from pgpony composite".utf8)
        let armored = try OpenPGPPacketBuilder.buildCompositeEncryptedMessage(
            plaintext: plaintext, recipient: rec, armor: true)

        let att = XCTAttachment(data: armored)
        att.name = "pgpony-composite-to-sq.asc"
        att.lifetime = .keepAlways
        add(att)

        let cipherRaw = try OpenPGPPacketParser.dearmor(String(decoding: armored, as: UTF8.self))
        let cp = try OpenPGPPacketParser.parsePackets(data: Array(cipherRaw))
        XCTAssertTrue(cp.contains { $0.tag == 1 && $0.body.first == 6 }, "expected a v6 PKESK")
        XCTAssertTrue(cp.contains { $0.tag == 18 && $0.body.first == 2 }, "expected a SEIPDv2 packet")
    }
